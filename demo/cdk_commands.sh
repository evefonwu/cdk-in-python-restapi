# Useful CDK in Python related commands when starting a new project:

```sh
$ mkdir project-name
$ cd project-name 
$ cdk init --language python
```

```sh
$ python3 -m venv .venv
$ source .venv/bin/activate
```

```sh
(.venv) $ pip install -r requirements.txt

```

```
(.venv) $ pip install boto3
(.venv) $ pip list
```
