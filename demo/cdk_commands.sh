# CDK in Python related commands used to start this CDK project:

```sh
$ mkdir inventory_api
$ cd inventory_api 
$ cdk init --language python
```

```sh
$ python3 -m venv .venv
$ source .venv/bin/activate
```

```sh
(.venv) $ pip install -r requirements.txt

```

```
(.venv) $ pip install boto3
(.venv) $ pip list
